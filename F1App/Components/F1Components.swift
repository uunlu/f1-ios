import SwiftUI

/// F1 themed UI components for the app
/// This namespace provides consistent, reusable components with F1 styling
public enum F1Components {
    // MARK: - Component Type Aliases for Convenience
    
    /// Championship badge component
    public typealias ChampionBadge = F1ChampionBadge
    
    /// Gradient card container
    public typealias GradientCard = F1GradientCard
    
    /// Circular icon component
    public typealias CircleIcon = F1CircleIcon
    
    /// Gradient divider
    public typealias GradientDivider = F1GradientDivider
    
    /// Labeled content container
    public typealias LabeledContent = F1LabeledContent
    
    /// Team color bar indicator
    public typealias TeamColorBar = F1TeamColorBar
    
    /// Season list item
    public typealias SeasonListItem = F1SeasonListItem
    
    /// Race winner item
    public typealias RaceWinnerItem = F1RaceWinnerItem
    
    /// Section header
    public typealias SectionHeader = F1SectionHeader
    
    /// Loading list item placeholder
    public typealias LoadingListItem = F1LoadingListItem
    
    /// Error view component
    public typealias ErrorView = F1ErrorView
    
    /// Offline banner component
    public typealias OfflineBanner = F1OfflineBanner
}
